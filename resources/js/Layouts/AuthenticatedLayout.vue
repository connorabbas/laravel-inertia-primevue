<script setup>
import { ref, useTemplateRef, onMounted, onUnmounted, watchEffect } from "vue";
import { useForm } from "@inertiajs/vue3";
import Toast from "primevue/toast";
import ApplicationLogo from "@/Components/ApplicationLogo.vue";
import Container from "@/Components/Container.vue";
import LinksMenu from "@/Components/PrimeVue/LinksMenu.vue";
import LinksMenuBar from "@/Components/PrimeVue/LinksMenuBar.vue";
import LinksPanelMenu from "@/Components/PrimeVue/LinksPanelMenu.vue";
import ToggleThemeButton from "@/Components/ToggleThemeButton.vue";

const currentRoute = route().current();
const logoutForm = useForm({});
function logout() {
    logoutForm.post(route("logout"));
}

// Main menu
const mainMenuItems = [
    {
        label: "Dashboard",
        route: route("dashboard"),
        active: currentRoute == "dashboard",
    },
];

// User menu (desktop)
const userMenu = useTemplateRef("user-menu");
const userMenuItems = [
    {
        label: "Profile",
        route: route("profile.edit"),
        icon: "pi pi-fw pi-user",
    },
    {
        label: "Log Out",
        icon: "pi pi-fw pi-sign-out",
        command: () => {
            logout();
        },
    },
];
const toggleUserMenu = (event) => {
    userMenu.value.childRef.toggle(event);
};

// Mobile menu (Drawer)
const homeMobileMenuItems = ref([
    {
        label: "Welcome",
        icon: "pi pi-home",
        route: route("welcome"),
        active: currentRoute == "welcome",
    },
    {
        label: "Dashboard",
        icon: "pi pi-th-large",
        route: route("dashboard"),
        active: currentRoute == "dashboard",
    },
]);
const mobileMenuOpen = ref(false);
const windowWidth = ref(window.innerWidth);
const updateWidth = () => {
    windowWidth.value = window.innerWidth;
};
onMounted(() => {
    window.addEventListener("resize", updateWidth);
});
onUnmounted(() => {
    window.removeEventListener("resize", updateWidth);
});
watchEffect(() => {
    if (windowWidth.value > 1024) {
        mobileMenuOpen.value = false;
    }
});
</script>

<template>
    <div>
        <div class="min-h-screen">
            <nav
                class="surface-overlay surface-border"
                :class="$slots.header ? 'border-bottom-1' : 'shadow-1'"
            >
                <!-- Primary Navigation Menu -->
                <Container>
                    <LinksMenuBar
                        :model="mainMenuItems"
                        :pt="{
                            root: {
                                class: 'px-0 py-1 border-0 rounded-none surface-overlay',
                            },
                            button: {
                                class: 'hidden',
                            },
                        }"
                    >
                        <template #start>
                            <!-- Logo -->
                            <div class="shrink-0 flex align-items-center mr-5">
                                <Link :href="route('welcome')">
                                    <ApplicationLogo
                                        class="h-3rem w-auto pt-1 surface-svg-fill"
                                    />
                                </Link>
                            </div>
                        </template>
                        <template #end>
                            <div
                                class="hidden lg:flex align-items-center ms-6 gap-2"
                            >
                                <div>
                                    <ToggleThemeButton
                                        text
                                        severity="secondary"
                                        rounded
                                    />
                                </div>
                                <!-- User Dropdown Menu -->
                                <div class="flex flex-column">
                                    <Button
                                        id="user-menu-btn"
                                        text
                                        severity="secondary"
                                        :label="$page.props.auth.user.name"
                                        icon="pi pi-angle-down"
                                        iconPos="right"
                                        @click="toggleUserMenu($event)"
                                    />
                                    <div
                                        id="user-menu-append"
                                        class="relative"
                                    ></div>
                                    <LinksMenu
                                        appendTo="#user-menu-append"
                                        :model="userMenuItems"
                                        popup
                                        ref="user-menu"
                                        :pt="{
                                            root: {
                                                class: 'left-auto top-0 right-0',
                                            },
                                        }"
                                    />
                                </div>
                            </div>

                            <!-- Mobile Hamburger -->
                            <div class="flex align-items-center lg:hidden">
                                <div class="relative">
                                    <Button
                                        text
                                        severity="secondary"
                                        icon="pi pi-bars"
                                        @click="mobileMenuOpen = true"
                                        :pt="{
                                            icon: {
                                                class: 'text-xl',
                                            },
                                        }"
                                    />
                                </div>
                            </div>
                        </template>
                    </LinksMenuBar>
                </Container>

                <!-- Mobile drawer menu -->
                <Sidebar v-model:visible="mobileMenuOpen" position="right">
                    <template #header>
                        <ToggleThemeButton text severity="secondary" rounded />
                    </template>
                    <div>
                        <div>
                            <div class="mb-5">
                                <p
                                    class="text-muted-color font-bold uppercase text-sm mb-2"
                                >
                                    Home
                                </p>
                                <LinksPanelMenu
                                    :model="homeMobileMenuItems"
                                    class="w-full"
                                />
                            </div>
                        </div>
                    </div>
                    <template #footer>
                        <div class="flex align-items-center gap-2">
                            <Link
                                :href="route('profile.edit')"
                                class="flex-auto"
                            >
                                <Button
                                    label="Profile"
                                    icon="pi pi-user"
                                    fluid
                                    severity="secondary"
                                    outlined
                                ></Button>
                            </Link>
                            <Button
                                label="Logout"
                                icon="pi pi-sign-out"
                                class="flex-auto"
                                severity="danger"
                                text
                                @click="logout"
                            ></Button>
                        </div>
                    </template>
                </Sidebar>
            </nav>

            <!-- Page Heading -->
            <header class="surface-overlay shadow-1" v-if="$slots.header">
                <Container>
                    <div class="py-3">
                        <slot name="header" />
                    </div>
                </Container>
            </header>

            <!-- Page Content -->
            <Toast position="top-center" />
            <main>
                <slot />
            </main>
        </div>
    </div>
</template>
